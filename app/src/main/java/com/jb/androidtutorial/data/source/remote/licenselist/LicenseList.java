public class LicenseList {

    String apiPrefixes;
    String name;
    int id;

    public String getApiPrefixes() {
        return apiPrefixes;
    }

    public void setApiPrefixes(String apiPrefixes) {
        this.apiPrefixes = apiPrefixes;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
